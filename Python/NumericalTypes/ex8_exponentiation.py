# Exponentiation helps us to find power of numbers.
# 3 exponent 4 -> 3 * 3 * 3 * 3
# 2 to the power of 3 -> 2 * 2 * 2
# square of 4 -> 4 * 4
# cube of 5 -> 5 * 5 * 5 

# ** 

print( 4 ** 3 ) # 4 * 4 * 4 -> 64

print(3 ** 2) # 3 * 3 -> 9

